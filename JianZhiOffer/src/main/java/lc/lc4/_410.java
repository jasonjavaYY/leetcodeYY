package lc.lc4;

import java.util.Arrays;

/*
* 分割数组的最大值
*
* 给定一个非负整数数组 nums 和一个整数 m ，你需要将这个数组分成 m 个非空的连续子数组。
设计一个算法使得这 m 个子数组各自和的最大值最小。
示例 1：
输入：nums = [7,2,5,10,8], m = 2
输出：18
解释：
一共有四种方法将 nums 分割为 2 个子数组。
其中最好的方式是将其分为 [7,2,5] 和 [10,8] 。
因为此时这两个子数组各自的和的最大值为18，在所有情况中最小。
示例 2：
输入：nums = [1,2,3,4,5], m = 2
输出：9
示例 3：
输入：nums = [1,4,4], m = 3
输出：4
*
* 方法一：动态规划
「将数组分割为 m 段，求……」是动态规划题目常见的问法。本题中，我们可以令 f[i][j] 表示将数组的前 i 个数分割为 j 段能得到的最大连续子数组和的最小值。
* 进行状态转移时，我们可以考虑第 j 段的具体范围，即我们可以枚举 k，其中前 k 个数被分割为 j−1 段，而第 k+1 到第 i 个数为第 j 段。
* 此时，这 j 段子数组中和的最大值，就等于 f[k][j−1] 与 sub(k+1,i) 中的较大值，其中 sub(i,j) 表示数组 nums 中下标落在区间 [i,j] 内的数的和。
由于我们要使得子数组中和的最大值最小，因此可以列出如下的状态转移方程：
f[i][j]= k=0 | min | i−1 {max(f[k][j−1],sub(k+1,i))}
对于状态 f[i][j]，由于我们不能分出空的子数组，因此合法的状态必须有 i≥j。对于不合法（i<j）的状态，由于我们的目标是求出最小值，
* 因此可以将这些状态全部初始化为一个很大的数。在上述的状态转移方程中，一旦我们尝试从不合法的状态 f[k][j−1] 进行转移，那么 max(⋯) 将会是大数，
* 就不会对最外层的 min{⋯} 产生影响。此外，我们还需要将 f[0][0] 的值初始化为 0。在上述的状态转移方程中，当 j=1 时，
* 唯一的可能性就是前 i 个数被分成了一段。如果枚举的 k=0，那么就代表着这种情况；如果 k!=0，对应的状态 f[k][0] 是一个不合法的状态，无法转移。
* 因此我们需要令 f[0][0]=0。最终的答案即为 f[n][m]。
* */
//动态规划 非负整数数组nums和整数m，将数组分成m个非空连续子数组。使m个子数组各自和的最大值最小
public class _410 {
    public int splitArray(int[] nums, int m) {
        int n = nums.length;//计算数组长度
        //f[i][j]表示将数组前i个数分为j段能得到的最大连续子数组和最小值
        int[][] f = new int[n + 1][m + 1];//因此f是[n+1]和[m+1]
        for (int i = 0; i <= n; i++) {//先把f数组填充成MAX
            Arrays.fill(f[i], Integer.MAX_VALUE);
        }//sub(i+1)表示数组nums下标从0到i内数的和
        int[] sub = new int[n + 1];//因此sub大小是n+1，sub[0]是0
        for (int i = 0; i < n; i++) {//计算sub数字每个位置的值
            sub[i + 1] = sub[i] + nums[i];
        }
        f[0][0] = 0;//0个字符分成0段，和的最小值是0
        for (int i = 1; i <= n; i++) {//i从1到n
            //j不能大于i，即长i的数组被分成超过i段是不可能的
            for (int j = 1; j <= Math.min(i, m); j++) {//因此j从1到i和m的较小值
                //枚举k，前k个数被分为 j−1 段，第k+1到第i个数为第j段，k从0到i-1
                //此时j段子数组中和的最大值，就是f[k][j−1] 与 sub(k+1,i)中较大值
                //再用这个较大值和f[i][j]判断谁更小，就更新f[i][j]
                for (int k = 0; k < i; k++) {
                    f[i][j] = Math.min(f[i][j], Math.max(f[k][j - 1], sub[i] - sub[k]));
                }
            }
        }
        return f[n][m];//最后返回f[n][m]
    }
}
